// Les opérateurs
// Nous allons présenter les opérateurs en JavaScript
// Les opérateurs permettent d'effectuer des calculs

//Opérateur + : permet d'additionner des nombres
let prix = 5 + 5;  // Résultat : 10

// Opérateur + permet de cocaténer des chaînes de caractères
let message = " Bonjour " + " Madame ";  // Résultat: Bonjour Madame

// Opérateur - : permet de soustraire des nombres
let calculs = 2 - 8;  // Résultat : 6

// Opérateur * : permet de multiplier des nombres
let multiple = 4*3; // résultat stocké : 12

// Opérateur / : permet de diviser des nombres
let division = 24/2; // résultat : 12

// Opération = : permet d'affecter la valeur à la variable en cours

/* Opérateur == : Permet de comparer deux les valeurs entre variable 
si les deux valeurs sont ègales, 
  alors JavaScript retourne la valeurs TRUE.
si les deux valeurs sont différentes, 
  alors JavaScript retour la valeurs FALSE.
*/

let comparaison = multiplication == division; // Est-ce que 12 = 12 ?
/* le signe === vérifier que la valeur soit identique 
et le typage soit identique. */
